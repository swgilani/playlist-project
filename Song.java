
public class Song {
	
	String name;
	String genre;
	String artist;
	String album;
	int length;
	int year;
	
	
	
	
	public Song(String name, String genre, String artist, String album, int length, int year) {
		
		this.name = name;
		this.genre = genre;
		this.artist = artist;
		this.album = album;
		this.length = length;
		this.year = year;
		
	}
	
	
}
